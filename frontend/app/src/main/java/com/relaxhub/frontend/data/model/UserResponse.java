package com.relaxhub.frontend.data.model;

public class UserResponse {

    private long id;
    private String fullName;
    private String email;
    private String phone;

    public long getId() {
        return id;
    }

    public String getFullName() {
        return fullName;
    }

    public String getEmail() {
        return email;
    }

    public String getPhone() {
        return phone;
    }
}
